#!/usr/bin/env python3
"""Fair benchmark on JFLEG — real human errors, not synthetic.

JFLEG (JHU FLuency-Extended GEC) contains 748 sentences written by ESL
learners with real spelling/grammar errors. We filter for spelling-focused
examples (minor word changes, not full grammar rewrites) and test all
systems on the same data.

This is a fair test: no system was trained on this data, and the errors
are natural human mistakes, not generated by our corruption engine.

Usage:
    CUDA_VISIBLE_DEVICES="" PYTHONPATH=. python eval/jfleg_benchmark.py \
        --model checkpoints/pretrain/step_70000.pt \
        --tokenizer tokenizer/tokenizer.model
"""

import argparse
import re
import subprocess
import time
from pathlib import Path

import torch
import sentencepiece as spm
from datasets import load_dataset

from model.architecture import NeuralSpellModel

BOS_ID = 2
EOS_ID = 3


def load_jfleg_spelling(max_examples: int = 0) -> list[tuple[str, list[str]]]:
    """Load JFLEG test set, filtered for spelling-focused examples.

    Returns (input_sentence, [reference_corrections]) pairs where
    the corrections are minor word-level changes (not full rewrites).
    """
    ds = load_dataset("jfleg", split="test")
    pairs = []
    for ex in ds:
        src = ex["sentence"]
        refs = ex["corrections"]
        src_words = src.lower().split()
        # Check against first reference for filtering
        ref_words = refs[0].lower().split()
        if abs(len(src_words) - len(ref_words)) > 2:
            continue
        if len(src_words) < 5:
            continue
        changes = sum(1 for a, b in zip(src_words, ref_words) if a != b)
        if 1 <= changes <= 5:
            pairs.append((src, refs))
            if max_examples and len(pairs) >= max_examples:
                break
    return pairs


def word_accuracy_multi_ref(predicted: str, references: list[str]) -> tuple[int, int]:
    """Score against multiple references, take the best match."""
    best_correct = 0
    best_total = 1
    for ref in references:
        pred_words = predicted.lower().split()
        ref_words = ref.lower().split()
        total = len(ref_words)
        correct = sum(1 for p, r in zip(pred_words, ref_words) if p == r)
        if total > 0 and correct / total > best_correct / best_total:
            best_correct = correct
            best_total = total
    return best_correct, best_total


# ─── System wrappers ─────────────────────────────────────────────

class NeuralSpellSystem:
    def __init__(self, model_path, tokenizer_path, device):
        self.device = device
        self.model = NeuralSpellModel()
        ckpt = torch.load(model_path, map_location=device, weights_only=False)
        sd = {k.replace("_orig_mod.", ""): v for k, v in ckpt["model_state_dict"].items()}
        self.model.load_state_dict(sd)
        self.model.to(device).eval()
        self.sp = spm.SentencePieceProcessor()
        self.sp.Load(str(tokenizer_path))
        step = ckpt.get("step", "?")
        self.name = f"NeuralSpell 385M (step {step})"

    def correct(self, sentence):
        ids = self.sp.Encode(sentence)[:256]
        inp = torch.tensor([ids], dtype=torch.long, device=self.device)
        mask = torch.ones_like(inp)
        with torch.no_grad():
            enc = self.model.encode(inp, mask)
        gen = [BOS_ID]
        for _ in range(256):
            dec = torch.tensor([gen], dtype=torch.long, device=self.device)
            dm = torch.ones_like(dec)
            with torch.no_grad():
                logits = self.model.decode(dec, dm, enc, mask)
            tok = logits[0, -1, :].argmax().item()
            if tok == EOS_ID:
                break
            gen.append(tok)
        return self.sp.Decode(gen[1:]).strip()


class BARTSystem:
    def __init__(self):
        from transformers import pipeline
        self.pipe = pipeline(
            "text2text-generation",
            model="oliverguhr/spelling-correction-english-base",
            device=-1,
        )
        self.name = "BART-base (139M)"

    def correct(self, sentence):
        return self.pipe(sentence, max_length=512)[0]["generated_text"].strip()


class T5LargeSystem:
    def __init__(self):
        from transformers import T5ForConditionalGeneration, AutoTokenizer
        self.tokenizer = AutoTokenizer.from_pretrained("ai-forever/T5-large-spell")
        self.model = T5ForConditionalGeneration.from_pretrained("ai-forever/T5-large-spell")
        self.model.eval()
        self.name = "T5-large-spell (770M)"

    def correct(self, sentence):
        inputs = self.tokenizer(sentence, return_tensors="pt", max_length=256, truncation=True)
        with torch.no_grad():
            out = self.model.generate(**inputs, max_length=256)
        result = self.tokenizer.decode(out[0], skip_special_tokens=True)
        if len(result) > len(sentence) * 1.5:
            result = result[:len(sentence) + 20].strip()
        return result


class AspellSystem:
    def __init__(self):
        self.name = "aspell (dictionary)"

    def correct(self, sentence):
        tokens = re.findall(r"[\w']+|[^\w\s]+|\s+", sentence)
        return "".join(
            self._correct_word(t) if re.match(r"[\w']+", t) and len(t) > 1 else t
            for t in tokens
        )

    def _correct_word(self, word):
        try:
            result = subprocess.run(
                ["aspell", "-a", "--lang=en"],
                input=word, capture_output=True, text=True, timeout=5,
            )
            for line in result.stdout.split("\n"):
                if line.startswith("&"):
                    parts = line.split(": ", 1)
                    if len(parts) > 1:
                        return parts[1].split(",")[0].strip()
                elif line.startswith("*"):
                    return word
            return word
        except (subprocess.TimeoutExpired, FileNotFoundError):
            return word


# ─── Main ────────────────────────────────────────────────────────

def main():
    parser = argparse.ArgumentParser(description="JFLEG benchmark — real human errors")
    parser.add_argument("--model", type=Path, required=True)
    parser.add_argument("--tokenizer", type=Path, required=True)
    parser.add_argument("--max-examples", type=int, default=100, help="Max examples (0=all)")
    parser.add_argument("--skip-t5", action="store_true")
    parser.add_argument("--verbose", action="store_true")
    args = parser.parse_args()

    device = torch.device("cuda" if torch.cuda.is_available() else "cpu")

    print("Loading JFLEG test set...")
    pairs = load_jfleg_spelling(max_examples=args.max_examples)
    print(f"  {len(pairs)} spelling-focused examples")

    print("Loading systems...")
    systems = []
    print("  NeuralSpell...")
    systems.append(NeuralSpellSystem(args.model, args.tokenizer, device))
    print("  BART-base...")
    systems.append(BARTSystem())
    if not args.skip_t5:
        print("  T5-large-spell...")
        systems.append(T5LargeSystem())
    print("  aspell...")
    systems.append(AspellSystem())

    results = {s.name: {"correct": 0, "total": 0, "time": 0.0, "perfect": 0} for s in systems}

    for i, (src, refs) in enumerate(pairs):
        for sys in systems:
            t0 = time.time()
            pred = sys.correct(src)
            elapsed = time.time() - t0
            results[sys.name]["time"] += elapsed

            c, t = word_accuracy_multi_ref(pred, refs)
            results[sys.name]["correct"] += c
            results[sys.name]["total"] += t
            if c == t:
                results[sys.name]["perfect"] += 1

            if args.verbose and i < 10:
                print(f"  [{sys.name}] {pred}")

        if args.verbose and i < 10:
            print(f"  Input: {src}")
            print(f"  Ref:   {refs[0]}")
            print()

        if (i + 1) % 10 == 0:
            print(f"  {i+1}/{len(pairs)}...", end="\r")

    # Summary
    n = len(pairs)
    print(f"\n\n{'=' * 75}")
    print(f"JFLEG BENCHMARK — Real Human Errors ({n} sentences)")
    print(f"{'=' * 75}")
    print(f"  {'System':40s} {'Word Acc':>10s} {'Perfect':>10s} {'Time':>8s}")
    print(f"  {'-'*40} {'-'*10} {'-'*10} {'-'*8}")

    for sys in systems:
        r = results[sys.name]
        acc = r["correct"] / r["total"] * 100 if r["total"] > 0 else 0
        pct_perfect = r["perfect"] / n * 100
        print(f"  {sys.name:40s} {acc:>8.1f}%  {r['perfect']:>4d}/{n} ({pct_perfect:.0f}%)  {r['time']:>6.1f}s")

    print(f"\n  Note: Scored against best of {len(pairs[0][1]) if pairs else 4} reference corrections per sentence.")
    print(f"  'Perfect' = all words match a reference exactly.")


if __name__ == "__main__":
    main()
